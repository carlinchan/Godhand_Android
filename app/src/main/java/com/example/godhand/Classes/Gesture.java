package com.example.godhand.Classes;

//////////////////////////////////////////////////////////////////////////////////////////
// Gesture Class
// Used for managing the Gesture object
//////////////////////////////////////////////////////////////////////////////////////////
public class Gesture {
    // Gesture's id
    private int id;
    // Gesture's name
    private String name;
    // Gesture's content (command)
    private String content;

    //////////////////////////////////////////////////////////////////////////////////////////
    // Constructor 1
    //////////////////////////////////////////////////////////////////////////////////////////
    public Gesture() {

    }

    //////////////////////////////////////////////////////////////////////////////////////////
    // Constructor 2
    // Para: n - name
    // Para: c - content (command)
    //////////////////////////////////////////////////////////////////////////////////////////
    public Gesture(String n, String c) {
        this.name = n;
        this.content = c;
    }

    //////////////////////////////////////////////////////////////////////////////////////////
    // Constructor 3
    // Para: i - id
    // Para: n - name
    // Para: c - content (command)
    //////////////////////////////////////////////////////////////////////////////////////////
    public Gesture(int i, String n, String c) {
        this.id = i;
        this.name = n;
        this.content = c;
    }

    //////////////////////////////////////////////////////////////////////////////////////////
    // Getters
    //////////////////////////////////////////////////////////////////////////////////////////
    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getContent() {
        return content;
    }

    //////////////////////////////////////////////////////////////////////////////////////////
    // setters
    //////////////////////////////////////////////////////////////////////////////////////////
    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setContent(String content) {
        this.content = content;
    }

}
